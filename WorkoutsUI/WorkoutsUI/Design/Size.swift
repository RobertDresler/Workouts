import CoreGraphics

public typealias Size = CGFloat

public extension Size {
    /// 0pt
    static let zero: Size = 0
    /// 2pt
    static let xxsmall: Size = 2
    /// 4pt
    static let xsmall: Size = 4
    /// 8pt
    static let small: Size = 8
    /// 12pt
    static let large: Size = 12
    /// 16pt
    static let xlarge: Size = 16
    /// 20pt
    static let xxlarge: Size = 20
    /// 24pt
    static let huge: Size = 24
    /// 32pt
    static let xhuge: Size = 32
    /// 48pt
    static let xxhuge: Size = 48
}
